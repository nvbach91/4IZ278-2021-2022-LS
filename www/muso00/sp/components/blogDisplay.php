        <h4>2. Rum</h4>
        <p>Rum is a drink that can be enjoyed over ice in a cocktail or poured neat over some after-dinner chocolates. It’s the perfect drink to share with
            friends or to enjoy by yourself. There are so many different types of rum available on the market that it can be hard to know which one is right for you.
